package FichaPraticaExtraCiclos;
import java.util.Scanner;
public class Ex_01 {
    public static void main(String[] args) {

        // Declarar variaveis
        int num = 1;

        // Condicao que fara print dos numeros entre 1 a 500
        for (int i = 1; i <= 500; i++) {
            System.out.println(num);
            num++;
        }
    }
}
