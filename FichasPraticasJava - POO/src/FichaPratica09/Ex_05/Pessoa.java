package FichaPratica09.Ex_05;

public class Pessoa {

    private String nome;
    private int idade;
    private String email;
    private String numTelemovel;

    public Pessoa(String nome, int idade, String email, String numTelemovel) {
        this.nome = nome;
        this.idade = idade;
        this.email = email;
        this.numTelemovel = numTelemovel;
    }
}
